function solution(s, n) {
    let arr = [...s];

    for (let i = 0; i < arr.length; i++) {
        let ASC = arr[i].codePointAt();

        if (ASC !== 32) {
            if (ASC >= 65 && ASC <= 90) {
                ASC = ((ASC - 65 + n) % 26) + 65;
            } else if (ASC >= 97 && ASC <= 122) {
                ASC = ((ASC - 97 + n) % 26) + 97;
            }
        }

        arr[i] = String.fromCharCode(ASC);
    }

    return arr.join("");
}